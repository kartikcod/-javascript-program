const number = prompt("enter the mark")
if (number <= 80) {
    console.log("a")
} else if (number <= 70) {
    console.log("b")
} else if (number <= 60) {
    console.log("c")
} else if (number <= 50) {
    console.log("d")
} else {
    console.log("f")
}